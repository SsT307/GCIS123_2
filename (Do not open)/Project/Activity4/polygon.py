class Polygon:
    def __init__(self, name, sides):
        self.name = name
        self.sides = sides
    
    def get_name(self):
        return self.name
    
    def set_name(self, name):
        self.name = name
    
    def get_sides(self):
        return self.sides
    
    def set_sides(self, sides):
        self.sides = sides
